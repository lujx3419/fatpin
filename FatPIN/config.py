#!/usr/bin/env python3
"""
配置文件 - 优化加速参数
"""

# 优化体素分辨率（mm）- 用于减少优化体素数以加速求解
# 说明：
#   - None: 使用默认分辨率（约5mm），体素数约30,755，速度慢但精度高
#   - 10.0: 降低分辨率到10mm，体素数约7,700，速度提升4-9倍，精度略降
#   - 15.0: 进一步降低到15mm，体素数约3,400，速度提升9-20倍，精度明显降低
#   - 建议：从10.0开始尝试，如果速度仍不够快，再尝试15.0
# 注意：手动降采样会导致PortPy索引不匹配错误，暂时禁用
# 如需加速，使用更快的求解器参数（已在代码中实现）
OPT_VOXEL_RESOLUTION_MM = None  # 禁用降采样，避免索引错误

# 求解器参数（进一步加速）
SOLVER_MAX_ITER = 1500  # 最大迭代次数（减少可加速但可能不收敛）
SOLVER_EPS_ABS = 5e-3   # 绝对容差（放宽可加速但精度降低）
SOLVER_EPS_REL = 5e-3   # 相对容差

